            <h2>Офис для сайта</h2>
            <p>Первым делом нужно определиться с выбором типа сервера, на котором будет стоять сайт, так как от этого
                напрямую будет зависеть скорость загрузки ресурса и его безопасность.</p>
            <p>Виртуальный хостинг — это услуга предоставления места на диске в соседстве с другими сайтами. Это самый
                дешёвый вариант, однако, для интернет-магазина его не стоит даже рассматривать. Он совершенно точно не
                сможет обеспечить достаточную скорость загрузки страницы. А уж о безопасности в этом случае нет смысла и
                говорить. </p>
            <p>Виртуальный сервер — это выделенное пространство на диске сервера. В этом случае вы гарантированно
                получаете запрошенные параметры, более высокую скорость загрузки, собственный IP. Его уже можно
                использовать для интернет-магазина, но только на начальных этапах. </p>
            <p>По мере развития сервиса и увеличения количества посетителей будет логично перейти на аренду физического
                сервера</p>
            <p>После этого нужно подобрать программное обеспечение, которое будет обеспечивать работу магазина. От этого
                будет зависеть функциональность ресурса и то, насколько удобен он будет для пользователей и
                администратора. Выбрать нужно следующие программы: </p>
            <ul>
                <li>• CMS;</li>
                <li>• CRM;</li>
                <li>• Программу складского учёта.</li>
