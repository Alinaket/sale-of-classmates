            <h2>Техническая сторона разработки интернет-магазина услуг</h2>
            <p>С этой точки зрения продажа услуг мало чем отличается от продажи товаров. Да, страница с описанием в этом
                случае будет называться карточкой услуг, а кнопка под описанием будет гласить не «купить», а «заказать
                услугу». И всё же с технической стороны это всё реализуется точно также, как в классическом
                интернет-магазине.</p>
            <p>В любом случае нужна витрина, на которой располагаются карточки с подробным описанием того, что
                посетитель может приобрести. А уж будет это товар или услуга, с точки зрения реализации, нет никакой
                разницы. Даже структура карточки будет примерно одинаковой.</p>
            <p>Разница будет, скорее, в том, какие дополнительные функции потребуются продавцу, а также в том, как
                именно будет продвигаться услуга.</p>
