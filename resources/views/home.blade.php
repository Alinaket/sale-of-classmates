            <h2>В чём сложности дистанционной продажи услуг?</h2>
            <p>Прежде чем купить что-то, человек предпочитает внимательно это осмотреть, пощупать, при необходимости
                померить. Это позволяет убедиться в том, что товар действительно подойдёт. С продажей через интернет
                сложнее, но здесь помогают
                выкрутиться красочные фотографии и подробное описание. И в любом случае на товар есть гарантия, его
                можно обменять или вернуть.</p>
            <p>С услугами всё сложнее. Это не материальный объект, а некое действие. По сути, человек платит за чужой
                профессионализм. Вот только как в этом профессионализме
                убедиться? Некачественно оказанную услугу не вернёшь: отрезанные волосы не приставишь назад, менять
                адвоката после проигрыша дела уже поздно. В случае с
                медицинскими услугами можно и вовсе навредить своему здоровью. </p>
            <p>В этой сфере наибольшую значимость имеют личные впечатления клиентов. Раньше это было сарафанное радио –
                друзья и знакомые рассказывали друг другу о мастерах,
                которые оправдали их ожидания. В эпоху интернета этот механизм всё еще работает, но теперь существуют и
                другие способы привлечь клиентов и продать им услугу. </p>
